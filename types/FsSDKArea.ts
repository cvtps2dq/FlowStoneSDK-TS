class FsSDKArea{
    private _x:Number;
    private _y:Number;
    private _w:Number;
    private _h:Number;

    get x(): Number {
        return this._x;
    }

    set x(value: Number) {
        this._x = value;
    }

    get y(): Number {
        return this._y;
    }

    set y(value: Number) {
        this._y = value;
    }

    get w(): Number {
        return this._w;
    }

    set w(value: Number) {
        this._w = value;
    }

    get h(): Number {
        return this._h;
    }

    set h(value: Number) {
        this._h = value;
    }
}
export default FsSDKArea;